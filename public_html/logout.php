<?php
/* =========================
   logout.php
   ========================= */

if (session_status() === PHP_SESSION_NONE) {
    session_start();
}

/*
|------------------------------------------------------------
| ניקוי כל משתני הסשן
|------------------------------------------------------------
*/
$_SESSION = [];

/*
|------------------------------------------------------------
| מחיקת קוקי של הסשן
|------------------------------------------------------------
*/
if (ini_get('session.use_cookies')) {
    $params = session_get_cookie_params();

    setcookie(
        session_name(),
        '',
        time() - 42000,
        $params['path'],
        $params['domain'],
        $params['secure'],
        $params['httponly']
    );
}

/*
|------------------------------------------------------------
| סיום הסשן
|------------------------------------------------------------
*/
session_destroy();

/*
|------------------------------------------------------------
| חזרה לאתר הרגיל אחרי התנתקות
|------------------------------------------------------------
| חשוב:
| אם נחזור רק ל־/ אז index.php יכול לזהות נייד
| ולהעביר אוטומטית ל־/mobile/.
|
| לכן מוסיפים desktop=1.
*/
/*
|------------------------------------------------------------
| סימון בדפדפן: אחרי logout להישאר באתר הרגיל
|------------------------------------------------------------
*/
setcookie('force_desktop_after_logout', '1', [
    'expires'  => time() + 300,
    'path'     => '/',
    'secure'   => !empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off',
    'httponly' => true,
    'samesite' => 'Lax'
]);

header('Location: /?desktop=1&from_logout=1');
exit;
exit;
